
public class swap {

	public static void main(String[] args)
	{
		int x=10;
		int y=20;
		x=x+y;//x=10+20//x=30
		y=x-y;//y=30-20//y=10
		x=x-y;//updated values x=30-10//so x=20
		System.out.println(x);
		System.out.println(y);
	
//we can do even multiplication and division using same concept
	}

}
